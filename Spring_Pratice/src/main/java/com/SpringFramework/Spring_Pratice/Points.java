package com.SpringFramework.Spring_Pratice;

public class Points {
	
	private int x,y,z;
	
	//Setters
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y){
		this.y = y;
	}
	public void setZ(int z) {
		this.z = z;
	}
		
	//Getters
	
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public int getZ() {
		return z;
	}
	
}
